void twoComplement(float val, int out[64]) {

    val = (val > (floorf(val) + 0.5f)) ? ceilf(val) : floorf(val);

    int ex = (int)floorf(log2f(val));
    out[ex] = 1;
    printf("%d-", ex);

    if ((val - pow(2, ex)) > 0) twoComplement((val - powf(2, (float)ex)), out);

}

